// Build lowercase index with "the " prefix variants for flexible matching
const _index = new Set();
for (const name of CONFIRMED_ARTISTS) {
  const lower = name.toLowerCase();
  _index.add(lower);
  // Also index without "the " prefix (and vice versa) so
  // "Rolling Stones" matches "The Rolling Stones" from catalog
  if (lower.startsWith('the ')) {
    _index.add(lower.slice(4));
  } else {
    _index.add('the ' + lower);
  }
}

export function isConfirmed(artistName) {
  return _index.has(artistName.toLowerCase());
}
